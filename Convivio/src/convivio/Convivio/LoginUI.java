    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String username = nomeTF.getText();
        char []prePassword = passwordTF.getPassword();
        String password = new String(prePassword);
        if(username == null || username.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Nome não pode estar vazio!", "InfoBox: Username", JOptionPane.INFORMATION_MESSAGE);
        }else if(password.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Passoword não pode estar vazia!", "InfoBox: Password", JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(username.equals("admin") && password.equals("admin")){
                this.setVisible(false);
                new AdminUI(importantFrame).setVisible(true);
                this.dispose();
            }else{
                Pessoa p = importantFrame.searchPessoa(username);
                if(p == null){
                    JOptionPane.showMessageDialog(null, "O nome que está a colocar não está correto!", "InfoBox: Pessoa não encontrada", JOptionPane.INFORMATION_MESSAGE);
                    nomeTF.setText("");
                    passwordTF.setText("");
                }else{
                    if(importantFrame.isPasswordCorrect(p, password)){
                        this.setVisible(false);
                        new ClientUI(importantFrame, p).setVisible(true);
                        this.dispose();
                    }else{
                         JOptionPane.showMessageDialog(null, "A password não está correta!", "InfoBox: Password errada", JOptionPane.INFORMATION_MESSAGE);
                         passwordTF.setText("");
                    }
                }
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

