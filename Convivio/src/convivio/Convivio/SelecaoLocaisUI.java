    private void inscreverButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inscreverButtonActionPerformed
        // TODO add your handling code here:
        ConvivioDei convivio = importantFrame.getConvivio(nomeConvivio);
        ArrayList<Local> listaTotal = convivio.getListaLocais();
        Local l = null;
        for(Local local : listaTotal){
            if(local.getNome().equals(todosLocaisComboBox.getSelectedItem().toString())){
                l = local;
                break;
            }
        }
        if(l != null){
            if(jList1.getModel().getSize() == 5){
                JOptionPane.showMessageDialog(null, "Só se pode inscrever em 5 locais!", "InfoBox: Excesso de locais", JOptionPane.INFORMATION_MESSAGE);
            }else{   
                boolean isAlreadyRegistered = false;
                ListModel model = jList1.getModel();
                for(int i=0; i < model.getSize(); i++){
                     if(model.getElementAt(i).equals(todosLocaisComboBox.getSelectedItem().toString())){
                         isAlreadyRegistered = true;
                         break;
                     }  
                }
                if(!isAlreadyRegistered){
                    int flag = importantFrame.inscreverEmLocal(convivio, pessoa, l);
                    if(flag == 1){
                        JOptionPane.showMessageDialog(null, "A inscrição foi feita com sucesso!", "InfoBox: Inscricao", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Não se consegui inscrever pois estava lotado!", "InfoBox: Inscricao", JOptionPane.INFORMATION_MESSAGE);
                    }
                    populateList();
                }else{
                    JOptionPane.showMessageDialog(null, "Já se inscreveu nessa opção!", "InfoBox: Repetição de locais", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }        
    }//GEN-LAST:event_inscreverButtonActionPerformed

    private void desinscreverButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_desinscreverButtonActionPerformed
        // TODO add your handling code here:
        ConvivioDei convivio = importantFrame.getConvivio(nomeConvivio);
        ArrayList<Local> listaTotal = convivio.getListaLocais();
        Local l = null;
        for(Local local : listaTotal){
            if(local.getNome().equals(jList1.getSelectedValue())){
                l = local;
                break;
            }
        }
        if(l != null){
            importantFrame.desisncreverDeLocal(convivio, pessoa, l);
            populateList();
        }
    }//GEN-LAST:event_desinscreverButtonActionPerformed

    /**
     * Adiciona os nomes dos locais do convívio à ArrayList.
     */
    private void populateArray(){
        ConvivioDei convivio = importantFrame.getConvivio(nomeConvivio);
        ArrayList<Local> listaTotal = convivio.getListaLocais();
        for(Local local : listaTotal){
            locais.add(local.getNome());
        }
    }
    
    /**
     * Adiciona à lista os nomes dos locais que o utlizador já está inscrito.
     */
    private void populateList(){
        DefaultListModel dlm = new DefaultListModel();
        ConvivioDei convivio = importantFrame.getConvivio(nomeConvivio);
        ArrayList<Inscricao> inscricoes = convivio.getInscricoes();
        for(Inscricao ins : inscricoes){
            if(pessoa.getNome().equals(ins.getNomePessoa())){
                dlm.addElement(ins.getNomeLocal());
            }
        }
        jList1.setModel(dlm);        
    }
    
