    private void listConviviosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listConviviosButtonActionPerformed
        // TODO add your handling code here:
        boolean open = false;
        ArrayList<ConvivioDei> listaConv = importantFrame.getListaConvivios();
        for(ConvivioDei conv : listaConv){
            if(conv.isPersonRegistered(pessoa.getNome())){
                open = true;
                break;
            }
        }
        if(open){
            this.setVisible(false);
            new ListaConviviosUI(importantFrame, this, pessoa).setVisible(true);
        }else{
            int reply = JOptionPane.showConfirmDialog(null, "Não tem nenhum Convivio onde está inscrito, pretende inscrever-se?", "InfoBox: Inscrições", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {         
                this.setVisible(false);
                new InscreverConvivioUI(importantFrame, this, pessoa).setVisible(true);
            }
        }
    }//GEN-LAST:event_listConviviosButtonActionPerformed

